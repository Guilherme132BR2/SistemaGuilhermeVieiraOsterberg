    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        habilitar();
        int resp = JOptionPane.showConfirmDialog(null, "Deseja excluir o registro",
                "confirmar", JOptionPane.YES_NO_OPTION);
        if (resp == JOptionPane.YES_OPTION)
        {
            Patrocinadores patrocinadores = viewBean();
            Patrocinadores_DAO patrocinadores_DAO = new Patrocinadores_DAO();
            patrocinadores_DAO.delete(patrocinadores);

        } else
        {
            JOptionPane.showMessageDialog(null, "Exclus√£o cancelada ",
                    "alerta", 2
            );
        }
        limparCampos();
    }//GEN-LAST:event_jBtnExcluirActionPerformed

