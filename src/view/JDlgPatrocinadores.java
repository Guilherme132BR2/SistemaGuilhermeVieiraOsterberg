  public Patrocinadores viewBean() {
        Patrocinadores patrocinadores = new Patrocinadores();
        int id = Integer.valueOf(jTxtIdPatrocinadores.getText());
        patrocinadores.setIdPatrocinadores(id);
        patrocinadores.setNomeEmpresa(jTxtNomeEmpresa.getText());
        patrocinadores.setCnpj(jFmtCnpj.getText());
        patrocinadores.setCelular(jFmtCelular.getText());
        patrocinadores.setEmail(jTxtEmail.getText());

        return patrocinadores;
    }

    public void beanView(Patrocinadores patrocinadores) {
        jTxtIdPatrocinadores.setText(String.valueOf(patrocinadores.getIdPatrocinadores()));
        jTxtNomeEmpresa.setText(patrocinadores.getNomeEmpresa());
        jFmtCnpj.setText(patrocinadores.getCnpj());
        jFmtCelular.setText(patrocinadores.getCelular());
        jTxtEmail.setText(patrocinadores.getEmail());

    }

