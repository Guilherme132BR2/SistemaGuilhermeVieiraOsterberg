    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        // TODO add your handling code here:
        String apelido = jTxtUsuario.getText();
        String senha = new String(jPwfSenha.getPassword());

        Usuarios_DAO usuarios_DAO = new Usuarios_DAO();

        if (usuarios_DAO.fazerLogin(apelido, senha) != null) {
            JOptionPane.showMessageDialog(null, "Usu치rio logado com sucesso!");

            JFrmPrincipal jFrmPrincipal = new JFrmPrincipal();
            jFrmPrincipal.setVisible(true);

            tentativas = 0; 
        } else {
            tentativas++;
            JOptionPane.showMessageDialog(null, "Usu치rio e/ou senha inv치lidos! Tentativa " + tentativas + "/3");

            if (tentativas >= 3) {
                JOptionPane.showMessageDialog(null, "Limte de tentativas Excedido.");
                System.exit(0);
            }
        }
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

